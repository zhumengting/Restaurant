package com.food.util;

import java.io.PrintWriter;

import javax.servlet.http.HttpServletResponse;

import net.sf.json.JSONObject;


public class ResponseUtil {

	public static void write(Object jsonObject,HttpServletResponse response)throws Exception{
		response.setContentType("text/html;charset=utf-8");//消息头，数据包格式
		PrintWriter out=response.getWriter();//io流
		out.println(jsonObject.toString());//向页面输
		out.flush();//刷新
		out.close();
	}
}
